import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { type ICommissionRuleSet } from '@/shared/model/commission-rule-set.model';

const baseApiUrl = 'api/commission-rule-sets';

export default class CommissionRuleSetService {
  public find(id: number): Promise<ICommissionRuleSet> {
    return new Promise<ICommissionRuleSet>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: ICommissionRuleSet): Promise<ICommissionRuleSet> {
    return new Promise<ICommissionRuleSet>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: ICommissionRuleSet): Promise<ICommissionRuleSet> {
    return new Promise<ICommissionRuleSet>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: ICommissionRuleSet): Promise<ICommissionRuleSet> {
    return new Promise<ICommissionRuleSet>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
